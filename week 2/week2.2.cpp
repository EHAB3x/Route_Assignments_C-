#include <iostream>

using namespace std;

int main(){
    int x;
    cout << "Enter Number Between 1 and 5" << endl;
    cin >> x;
    if (x == 1)
    {
        cout << "One" << endl;
    }else if(x == 2){
        cout << "Two" << endl;
    }else if(x == 3){
        cout << "Three" << endl;
    }else if(x == 4){
        cout << "Four" << endl;
    }else if(x == 5){
        cout << "Five" << endl;
    }
    
}